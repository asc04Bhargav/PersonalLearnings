public class Calculate {
    public void add(byte n1, byte n2){
        System.out.println("byte sum: " + (n1+n2));
    }

    public void add(short n1, short n2){
        System.out.println("short sum: " + (n1+n2));
    }
    public void add(int n1, int n2){
        System.out.println("int sum: " + (n1+n2));
    }
    public void add(long n1, long n2){
        System.out.println("long sum: " + (n1+n2));
    }
}
